<script>
import { TweenMax } from "gsap/TweenMax";
import $ from "jquery";
import Scrollbar from "smooth-scrollbar";

export default {
  head: {
    title:
      "Raxo Professional Design Work Portfolio of Motion Graphics, Animation",
    meta: [
      {
        hid: "description",
        name: "description",
        content:
          "Browse our top work portfolio of motion graphics, digital marketing, graphic design, professional web and animation design. Contact Raxo team to discuss your project."
      },
      {
        hid: "keyword",
        name: "keyword",
        content:
          "motion graphics portfolio, digital marketing portfolio, graphic design portfolio, professional design work, animation design portfolio"
      }
    ]
  },
  mounted() {
    setTimeout(
      function() {
        Scrollbar.init(document.getElementById("smoothie"), {});

        const scrollbar = Scrollbar.get(document.getElementById("smoothie"));

        let topCenter = 0;
        let mouse = 0;
        let hoverSolution = 0;
        let contH = 0;

        const x = document.getElementById("lista").getBoundingClientRect();
        (topCenter = x.top),
          (contH = x.height),
          document
            .getElementById("lista")
            .addEventListener("mousemove", function(t) {
              mouse = t.clientY - topCenter;
              const e =
                $(".row_work")
                  .parent()
                  .height() - contH;

              (this.transform = (-mouse * e) / contH),
                TweenMax.to($(".row_work").parent(), 0.1, {
                  y: this.transform
                });
            });

        $(".work_list").scroll(function() {
          const t = document.getElementById("lista").getBoundingClientRect()
            .height;
          const e = document
            .getElementById("lista")
            .getElementsByTagName("div")[0]
            .getBoundingClientRect().height;
          const total = document
            .getElementById("lista")
            .getElementsByTagName("div")[0].children.length;
          const n =
            ($(this).scrollTop() * ((t + window.innerHeight) / total)) / e;
          hoverSolution = Math.ceil(n);
          if (hoverSolution < total) {
            $(".row_work")
              .eq(hoverSolution)
              .addClass("work_active");
            if ($(".row_work").hasClass("work_active")) {
              $(".row_work").removeClass("work_active");
              $(".imagenes div").removeClass("hero_active");
            }
            // $(this).addClass('work_active');
            $(".row_work")
              .eq(hoverSolution)
              .addClass("work_active");
            $(".imagenes div.row_hero")
              .eq(hoverSolution)
              .addClass("hero_active");
          }
        });

        $("#lista div .row_work").mouseenter(function() {
          if ($(".row_work").hasClass("work_active")) {
            $(".row_work").removeClass("work_active");
            $(".imagenes div").removeClass("hero_active");
          }
          $(this).addClass("work_active");
          $(".imagenes div.row_hero")
            .eq($(this).index())
            .addClass("hero_active");
        });

        $(".work_title")
          .mouseenter(function() {
            $(".raxo_pointer").addClass("pointer_on");
          })
          .mouseleave(function() {
            $(".raxo_pointer").removeClass("pointer_on");
          });

        // pointer_on
        window.addEventListener("pageshow", function(event) {
          const historyTraversal =
            event.persisted ||
            (typeof window.performance !== "undefined" &&
              window.performance.navigation.type === 2);
          if (historyTraversal) {
            // Handle page restore.
            window.location.reload();
          }
        });

        $(".herobg").each(function(index, el) {
          new Image().src = $(this)
            .css("background-image")
            .replace(/^url\(['"](.+)['"]\)/, "$1");
        });

        $(window).on("load", function() {
          const tl = new TimelineMax({
            // repeat: -1,
            delay: 1
          });

          $("#smoothie").removeAttr("cloak");
          tl.add("start")
            .staggerFrom(
              "[data-slide]",
              1.2,
              {
                autoAlpha: 0,
                ease: Expo.easeInOut,
                height: 0,
                clearProps: "all"
              },
              0.07,
              "start"
            )
            .staggerFrom(
              "[data-title]",
              0.6,
              {
                autoAlpha: 0,
                y: 20
              },
              0.05,
              "start"
            );
        });

        $(document).on("click", ".link_work", function(e) {
          e.preventDefault();
          var tl = new TimelineMax({
            onCompleteParams: [$(this).attr("href")],
            onComplete: redirect
          });
          tl.add("start")
            .add(
              TweenMax.to(
                "[data-slide]",
                1.2,
                {
                  autoAlpha: 0,
                  ease: Expo.easeInOut,
                  height: 0
                },
                "start"
              )
            )
            .set("[data-title]", {
              pointerEvents: "none"
            })
            .staggerTo(
              "[data-title]",
              0.4,
              {
                autoAlpha: 0,
                y: -20
              },
              0.03,
              "start"
            );
        });
        function redirect(redirect) {
          location.href = redirect;
          //window.location.replace(redirect);
        }
      }.bind(this),
      1300
    );
  }
};
</script>
